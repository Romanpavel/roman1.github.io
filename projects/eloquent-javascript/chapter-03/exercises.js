////////////////////////////////////////////////////////////////////////////////

// 2 parameters of a and b
function min(a, b) {
  
  // I : 2 numbers
  // O : the smaller number
  
  // test comparison a and b
  if (a < b) {
    return a;
  } else {
    
    // if a is not smaller b is smaller
    return b;
  }

}

